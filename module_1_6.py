# Создаем переменную и присваиваем множество
my_set={1,1,1,4,4,5,6,6,5,4,"a","s","d","s","a"}
print(my_set)
# добавить 2 произвольных элемента во множество
my_set.add(8.99)
my_set.add(9)
# выводим на экран добавленное множество
print(my_set)
# удаляем один элемент из множества (6)
print(my_set.remove(6))
# вывод на экран множества с удаленным значением
print(my_set)

