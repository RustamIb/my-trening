# Создаем словарь из нескольких пар ключ-значение
my_dict={"Иван":1995,"Василий":1972,"Сергей":1971,"Лариса":1972}
# выводим на экран
print(my_dict)
# выводим на экран одно значение по существующему ключу
print(my_dict["Сергей"])
# выводим на экран одно значение по отсутствующему ключу
my_dict["Николай"]=1988
print(my_dict)
# метод "update", для добавления нескольких пар
my_dict.update({"Валера":1985,"Леонид":1986})
print(my_dict)
# выводим один ключ из словаря
a=my_dict.pop("Василий")
# Выводим на экран словарь
print(my_dict)
# выводим значение выведенного из словаря ключа
print(a)
